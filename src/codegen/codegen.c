/* ─────────────────────────────────────────
   VARIABLE TABLE
───────────────────────────────────────── */
typedef struct VarOffset {
    char* name;
    int   offset;
    int   is_string;
    int   is_list;
    int   is_bool;
    struct VarOffset* next;
} VarOffset;

static VarOffset* var_list      = NULL;
static int        current_offset = 0;
static char       data_section[10000] = "";
static int        str_count     = 0;

static const char* ARG_REGS[] = { "rdi", "rsi", "rdx", "rcx", "r8", "r9" };
#define MAX_REG_ARGS 6

static VarOffset* find_var(const char* name) {
    for (VarOffset* v = var_list; v; v = v->next)
        if (strcmp(v->name, name) == 0) return v;
    return NULL;
}
static int get_variable_offset   (const char* n){ VarOffset* v=find_var(n); return v?v->offset:-1; }
static int get_variable_is_string(const char* n){ VarOffset* v=find_var(n); return v?v->is_string:0; }
static int get_variable_is_list  (const char* n){ VarOffset* v=find_var(n); return v?v->is_list:0; }
static int get_variable_is_bool  (const char* n){ VarOffset* v=find_var(n); return v?v->is_bool:0; }

static void add_variable(const char* name, int offset,
                         int is_string, int is_list, int is_bool) {
    VarOffset* v = malloc(sizeof(VarOffset));
    v->name=strdup(name); v->offset=offset;
    v->is_string=is_string; v->is_list=is_list; v->is_bool=is_bool;
    v->next=var_list; var_list=v;
}
static void free_var_list(void) {
    VarOffset* c=var_list;
    while(c){VarOffset* n=c->next;free(c->name);free(c);c=n;}
    var_list=NULL; current_offset=0;
}

/* ─────────────────────────────────────────
   LOOP LABEL STACK
   Used so that  break  and  skip  inside a
   nested loop know which labels to jump to.

   Each loop pushes its start/end labels onto
   the stack before generating its body, then
   pops them after.

     break  →  jmp  .L<end>    (exit loop)
     skip   →  jmp  .L<continue_target>

   For a  while  loop:
     continue_target = start  (re-evaluate condition)

   For a  for  loop:
     continue_target = update label  (run i=i+1 first)
───────────────────────────────────────── */
#define MAX_LOOP_DEPTH 64

typedef struct {
    int break_label;     /* label to jump to on break  */
    int continue_label;  /* label to jump to on skip   */
} LoopFrame;

static LoopFrame loop_stack[MAX_LOOP_DEPTH];
static int       loop_depth = 0;

static void push_loop(int break_lbl, int continue_lbl) {
    if (loop_depth >= MAX_LOOP_DEPTH) {
        fprintf(stderr, "CodeGen: loop nesting too deep\n"); exit(1);
    }
    loop_stack[loop_depth].break_label    = break_lbl;
    loop_stack[loop_depth].continue_label = continue_lbl;
    loop_depth++;
}

static void pop_loop(void) {
    if (loop_depth > 0) loop_depth--;
}

static int current_break_label(void) {
    if (loop_depth == 0) { fprintf(stderr,"CodeGen: break outside loop\n"); exit(1); }
    return loop_stack[loop_depth-1].break_label;
}

static int current_continue_label(void) {
    if (loop_depth == 0) { fprintf(stderr,"CodeGen: skip outside loop\n"); exit(1); }
    return loop_stack[loop_depth-1].continue_label;
}

/* ─────────────────────────────────────────
   HELPERS
───────────────────────────────────────── */
static int new_label(CodeGenerator* g) { return g->label_counter++; }

static int register_string(const char* value) {
    int id = str_count++;
    char buf[1024];
    snprintf(buf, sizeof(buf), "str%d db \"%s\", 0\n", id, value);
    strncat(data_section, buf, sizeof(data_section)-strlen(data_section)-1);
    return id;
}

static int bool_str_yes_id = -1;
static int bool_str_no_id  = -1;

/* ─────────────────────────────────────────
   PROLOGUE
───────────────────────────────────────── */
static void generate_prologue(CodeGenerator* g) {
    fprintf(g->output, "; Generated by Simple Compiler\n\n");
    fprintf(g->output, "section .text\nglobal _start\n\n");

    /* print_int */
    fprintf(g->output,
        "print_int:\n"
        "    push rbp\n    mov  rbp, rsp\n    sub  rsp, 64\n"
        "    mov  rax, rdi\n    mov  r8,  0\n    mov  rbx, 10\n"
        ".pi_convert:\n"
        "    xor  rdx, rdx\n    div  rbx\n    add  dl, '0'\n"
        "    dec  rsp\n    mov  [rsp], dl\n    inc  r8\n"
        "    test rax, rax\n    jnz  .pi_convert\n"
        ".pi_print:\n"
        "    test r8, r8\n    jz   .pi_newline\n"
        "    mov  rax, 1\n    mov  rdi, 1\n    mov  rsi, rsp\n    mov  rdx, 1\n"
        "    syscall\n    inc  rsp\n    dec  r8\n    jmp  .pi_print\n"
        ".pi_newline:\n"
        "    mov  byte [rbp-1], 10\n"
        "    mov  rax, 1\n    mov  rdi, 1\n    lea  rsi, [rbp-1]\n    mov  rdx, 1\n"
        "    syscall\n    mov  rsp, rbp\n    pop  rbp\n    ret\n\n"
    );

    /* print_str — save rdi into rsi BEFORE overwriting rdi with fd=1 */
    fprintf(g->output,
        "print_str:\n"
        "    push rbp\n    mov  rbp, rsp\n    sub  rsp, 16\n"
        "    mov  r8, 0\n"
        ".ps_len:\n"
        "    mov  al, [rdi + r8]\n    test al, al\n    jz   .ps_write\n"
        "    inc  r8\n    jmp  .ps_len\n"
        ".ps_write:\n"
        "    test r8, r8\n    jz   .ps_newline\n"
        "    mov  rax, 1\n"
        "    mov  rsi, rdi\n"   /* string ptr → rsi FIRST */
        "    mov  rdx, r8\n"
        "    mov  rdi, 1\n"     /* fd = stdout */
        "    syscall\n"
        ".ps_newline:\n"
        "    mov  byte [rbp-1], 10\n"
        "    mov  rax, 1\n    mov  rdi, 1\n    lea  rsi, [rbp-1]\n    mov  rdx, 1\n"
        "    syscall\n    mov  rsp, rbp\n    pop  rbp\n    ret\n\n"
    );

    /* list_alloc */
    fprintf(g->output,
        "list_alloc:\n"
        "    push rbp\n    mov  rbp, rsp\n"
        "    push rbx\n    push r12\n    push r13\n"
        "    mov  r12, rdi\n    mov  r13, rdi\n    imul r13, 2\n"
        "    cmp  r13, 8\n    jge  .la_cap_ok\n    mov  r13, 8\n"
        ".la_cap_ok:\n"
        "    xor  rdi, rdi\n    mov  rax, 12\n    syscall\n"
        "    mov  rbx, rax\n"
        "    mov  rdi, rax\n    mov  rcx, r13\n    imul rcx, 8\n"
        "    add  rdi, rcx\n    add  rdi, 16\n    mov  rax, 12\n    syscall\n"
        "    mov  [rbx],     r12\n    mov  [rbx + 8], r13\n    mov  rax, rbx\n"
        "    pop  r13\n    pop  r12\n    pop  rbx\n"
        "    mov  rsp, rbp\n    pop  rbp\n    ret\n\n"
    );

    /* list_push */
    fprintf(g->output,
        "list_push:\n"
        "    push rbp\n    mov  rbp, rsp\n"
        "    push rbx\n    push r12\n    push r13\n    push r14\n"
        "    mov  rbx, rdi\n    mov  r12, rsi\n"
        "    mov  r13, [rbx]\n    mov  r14, [rbx + 8]\n"
        "    cmp  r13, r14\n    jl   .lp_has_space\n"
        "    imul r14, 2\n    mov  [rbx + 8], r14\n"
        "    xor  rdi, rdi\n    mov  rax, 12\n    syscall\n"
        "    mov  rdi, rax\n    mov  rcx, r14\n    imul rcx, 8\n"
        "    add  rdi, rcx\n    add  rdi, 16\n    mov  rax, 12\n    syscall\n"
        ".lp_has_space:\n"
        "    mov  rcx, r13\n    imul rcx, 8\n    add  rcx, 16\n"
        "    mov  [rbx + rcx], r12\n    inc  r13\n    mov  [rbx], r13\n"
        "    pop  r14\n    pop  r13\n    pop  r12\n    pop  rbx\n"
        "    mov  rsp, rbp\n    pop  rbp\n    ret\n\n"
    );

    /* list_len */
    fprintf(g->output, "list_len:\n    mov  rax, [rdi]\n    ret\n\n");
}

