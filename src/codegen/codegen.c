#include "codegen.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

typedef struct VarOffset {
    char* name;
    int offset;
    struct VarOffset* next;
} VarOffset;

static VarOffset* var_list = NULL;
static int current_offset = 0;

static void generate_prologue(CodeGenerator* generator) {
    fprintf(generator->output, "; Generated by Simple C Compiler\n\n");
    fprintf(generator->output, "section .text\n");
    fprintf(generator->output, "global _start\n\n");
}

static int new_label(CodeGenerator* generator) {
    return generator->label_counter++;
}

static int get_variable_offset(const char* name) {
    VarOffset* curr = var_list;

    while (curr) {
        if (strcmp(curr->name, name) == 0)
            return curr->offset;
        curr = curr->next;
    }

    return -1;
}

static void add_variable_offset(const char* name, int offset) {
    VarOffset* v = malloc(sizeof(VarOffset));

    v->name = strdup(name);
    v->offset = offset;
    v->next = var_list;

    var_list = v;
}

static void generate_expression(CodeGenerator* generator, ASTNode* expr) {

    if (!expr) return;

    switch (expr->type) {

        case AST_INTEGER:
            fprintf(generator->output, "    mov rax, %d\n",
                    expr->data.int_value);
            break;

        case AST_IDENTIFIER: {

            int offset = get_variable_offset(expr->data.identifier);

            if (offset < 0) {
                fprintf(stderr,
                        "CodeGen Error: Undefined variable %s\n",
                        expr->data.identifier);
                exit(1);
            }

            fprintf(generator->output,
                    "    mov rax, [rbp - %d]\n", offset);
            break;
        }

        case AST_BINARY_OP: {

            TokenType op = expr->data.binary.op;

            /* LEFT expression */
            generate_expression(generator, expr->data.binary.left);
            fprintf(generator->output, "    push rax\n");

            /* RIGHT expression */
            generate_expression(generator, expr->data.binary.right);
            fprintf(generator->output, "    mov rbx, rax\n");
            fprintf(generator->output, "    pop rax\n");

            switch (op) {

                case TOKEN_PLUS:
                    fprintf(generator->output,
                            "    add rax, rbx\n");
                    break;

                case TOKEN_MINUS:
                    fprintf(generator->output,
                            "    sub rax, rbx\n");
                    break;

                case TOKEN_STAR:
                    fprintf(generator->output,
                            "    imul rax, rbx\n");
                    break;

                case TOKEN_SLASH:
                    fprintf(generator->output,
                            "    cqo\n");
                    fprintf(generator->output,
                            "    idiv rbx\n");
                    break;

                case TOKEN_GREATER:
                    fprintf(generator->output,
                            "    cmp rax, rbx\n");
                    fprintf(generator->output,
                            "    setg al\n");
                    fprintf(generator->output,
                            "    movzx rax, al\n");
                    break;

                case TOKEN_LESS:
                    fprintf(generator->output,
                            "    cmp rax, rbx\n");
                    fprintf(generator->output,
                            "    setl al\n");
                    fprintf(generator->output,
                            "    movzx rax, al\n");
                    break;

                default:
                    fprintf(stderr,
                            "CodeGen Error: Unsupported operator\n");
                    exit(1);
            }

            break;
        }

        default:
            break;
    }
}

static void generate_statement(CodeGenerator* generator, ASTNode* stmt) {

    if (!stmt) return;

    switch (stmt->type) {

        case AST_VAR_DECL: {

            current_offset += 8;

            add_variable_offset(
                stmt->data.var_decl.name,
                current_offset
            );

            fprintf(generator->output,
                    "    sub rsp, 8\n");

            if (stmt->data.var_decl.initializer) {

                generate_expression(
                    generator,
                    stmt->data.var_decl.initializer
                );

                fprintf(generator->output,
                        "    mov [rbp - %d], rax\n",
                        current_offset);
            }

            break;
        }

        case AST_ASSIGNMENT: {

            int offset =
                get_variable_offset(stmt->data.assignment.name);

            if (offset < 0) {
                fprintf(stderr,
                        "CodeGen Error: Undefined variable %s\n",
                        stmt->data.assignment.name);
                exit(1);
            }

            generate_expression(
                generator,
                stmt->data.assignment.value
            );

            fprintf(generator->output,
                    "    mov [rbp - %d], rax\n",
                    offset);

            break;
        }

        case AST_RETURN: {

            generate_expression(
                generator,
                stmt->data.return_value
            );

            fprintf(generator->output,
                    "    mov rdi, rax\n");

            fprintf(generator->output,
                    "    jmp .L_return\n");

            break;
        }

        case AST_IF: {

            int else_label = new_label(generator);
            int end_label = new_label(generator);

            generate_expression(
                generator,
                stmt->data.if_stmt.condition
            );

            fprintf(generator->output,
                    "    cmp rax, 0\n");

            fprintf(generator->output,
                    "    je .L%d\n",
                    else_label);

            generate_statement(
                generator,
                stmt->data.if_stmt.then_branch
            );

            fprintf(generator->output,
                    "    jmp .L%d\n",
                    end_label);

            fprintf(generator->output,
                    ".L%d:\n",
                    else_label);

            if (stmt->data.if_stmt.else_branch)
                generate_statement(
                    generator,
                    stmt->data.if_stmt.else_branch
                );

            fprintf(generator->output,
                    ".L%d:\n",
                    end_label);

            break;
        }

        case AST_WHILE: {

            int start = new_label(generator);
            int end = new_label(generator);

            fprintf(generator->output,
                    ".L%d:\n",
                    start);

            generate_expression(
                generator,
                stmt->data.while_stmt.condition
            );

            fprintf(generator->output,
                    "    cmp rax, 0\n");

            fprintf(generator->output,
                    "    je .L%d\n",
                    end);

            generate_statement(
                generator,
                stmt->data.while_stmt.body
            );

            fprintf(generator->output,
                    "    jmp .L%d\n",
                    start);

            fprintf(generator->output,
                    ".L%d:\n",
                    end);

            break;
        }

        case AST_BLOCK: {

            for (int i = 0;
                 i < stmt->data.block.statement_count;
                 i++) {

                generate_statement(
                    generator,
                    stmt->data.block.statements[i]
                );
            }

            break;
        }

        default:
            break;
    }
}

CodeGenerator* create_code_generator(const char* filename) {

    CodeGenerator* g =
        safe_malloc(sizeof(CodeGenerator));

    g->output = fopen(filename, "w");

    if (!g->output) {
        fprintf(stderr,
                "Failed to open output file\n");
        exit(1);
    }

    g->label_counter = 0;

    var_list = NULL;
    current_offset = 0;

    return g;
}

void free_code_generator(CodeGenerator* generator) {

    if (!generator) return;

    if (generator->output)
        fclose(generator->output);

    VarOffset* curr = var_list;

    while (curr) {

        VarOffset* next = curr->next;

        free(curr->name);
        free(curr);

        curr = next;
    }

    free(generator);
}

void generate_code(CodeGenerator* generator, ASTNode* program) {

    generate_prologue(generator);

    if (program->type == AST_PROGRAM) {

        for (int i = 0;
             i < program->data.program.function_count;
             i++) {

            ASTNode* func =
                program->data.program.functions[i];

            if (strcmp(
                    func->data.function.name,
                    "main"
                ) == 0) {

                fprintf(generator->output,
                        "_start:\n");

                fprintf(generator->output,
                        "    push rbp\n");

                fprintf(generator->output,
                        "    mov rbp, rsp\n");

                generate_statement(
                    generator,
                    func->data.function.body
                );

                fprintf(generator->output,
                        ".L_return:\n");

                fprintf(generator->output,
                        "    mov rsp, rbp\n");

                fprintf(generator->output,
                        "    pop rbp\n");

                fprintf(generator->output,
                        "    mov rax, 60\n");

                fprintf(generator->output,
                        "    syscall\n");
            }
        }
    }

    printf("Code generation completed: output.asm\n");
}
