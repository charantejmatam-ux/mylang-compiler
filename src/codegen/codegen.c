/* ── emit_output ── */
static void emit_output(CodeGenerator* g, ASTNode* arg) {
    if (!arg) return;

    if (arg->type == AST_STRING) {
        int id = register_string(arg->data.string_value);
        fprintf(g->output, "    lea  rdi, [rel str%d]\n    call print_str\n", id);
        return;
    }
    if (arg->type == AST_BOOL) {
        if (bool_str_yes_id < 0) bool_str_yes_id = register_string("yes");
        if (bool_str_no_id  < 0) bool_str_no_id  = register_string("no");
        int id = arg->data.bool_value ? bool_str_yes_id : bool_str_no_id;
        fprintf(g->output, "    lea  rdi, [rel str%d]\n    call print_str\n", id);
        return;
    }
    if (arg->type == AST_IDENTIFIER) {
        int off     = get_variable_offset(arg->data.identifier);
        int is_str  = get_variable_is_string(arg->data.identifier);
        int is_bool = get_variable_is_bool(arg->data.identifier);
        if (off < 0) { fprintf(stderr,"Undefined '%s'\n",arg->data.identifier); exit(1); }

        if (is_str) {
            fprintf(g->output, "    mov  rdi, [rbp - %d]\n    call print_str\n", off);
            return;
        }
        if (is_bool) {
            if (bool_str_yes_id < 0) bool_str_yes_id = register_string("yes");
            if (bool_str_no_id  < 0) bool_str_no_id  = register_string("no");
            int l_yes = new_label(g), l_end = new_label(g);
            fprintf(g->output, "    mov  rax, [rbp - %d]\n", off);
            fprintf(g->output, "    cmp  rax, 0\n    jne  .L%d\n", l_yes);
            fprintf(g->output, "    lea  rdi, [rel str%d]\n    call print_str\n", bool_str_no_id);
            fprintf(g->output, "    jmp  .L%d\n", l_end);
            fprintf(g->output, ".L%d:\n", l_yes);
            fprintf(g->output, "    lea  rdi, [rel str%d]\n    call print_str\n", bool_str_yes_id);
            fprintf(g->output, ".L%d:\n", l_end);
            return;
        }
        fprintf(g->output, "    mov  rdi, [rbp - %d]\n    call print_int\n", off);
        return;
    }
    /* any other expression → evaluate → print_int */
    generate_expression(g, arg);
    fprintf(g->output, "    mov  rdi, rax\n    call print_int\n");
}

/* ── expression generator ── */
static void generate_expression(CodeGenerator* g, ASTNode* expr) {
    if (!expr) return;
    switch (expr->type) {

        case AST_INTEGER:
            fprintf(g->output, "    mov  rax, %d\n", expr->data.int_value); break;

        case AST_BOOL:
            fprintf(g->output, "    mov  rax, %d\n", expr->data.bool_value); break;

        case AST_CHAR:
            fprintf(g->output, "    mov  rax, %d\n", (int)expr->data.char_value); break;

        case AST_STRING: {
            int id = register_string(expr->data.string_value);
            fprintf(g->output, "    lea  rax, [rel str%d]\n", id); break;
        }

        case AST_IDENTIFIER: {
            int off = get_variable_offset(expr->data.identifier);
            if (off < 0) { fprintf(stderr,"Undefined '%s'\n",expr->data.identifier); exit(1); }
            fprintf(g->output, "    mov  rax, [rbp - %d]\n", off); break;
        }

        case AST_BINARY_OP: {
            generate_expression(g, expr->data.binary.left);
            fprintf(g->output, "    push rax\n");
            generate_expression(g, expr->data.binary.right);
            fprintf(g->output, "    mov  rbx, rax\n    pop  rax\n");
            switch (expr->data.binary.op) {
                case TOKEN_PLUS:          fprintf(g->output,"    add  rax, rbx\n"); break;
                case TOKEN_MINUS:         fprintf(g->output,"    sub  rax, rbx\n"); break;
                case TOKEN_STAR:          fprintf(g->output,"    imul rax, rbx\n"); break;
                case TOKEN_SLASH:         fprintf(g->output,"    cqo\n    idiv rbx\n"); break;
                case TOKEN_GREATER:       fprintf(g->output,"    cmp rax,rbx\n    setg  al\n    movzx rax,al\n"); break;
                case TOKEN_LESS:          fprintf(g->output,"    cmp rax,rbx\n    setl  al\n    movzx rax,al\n"); break;
                case TOKEN_GREATER_EQUAL: fprintf(g->output,"    cmp rax,rbx\n    setge al\n    movzx rax,al\n"); break;
                case TOKEN_LESS_EQUAL:    fprintf(g->output,"    cmp rax,rbx\n    setle al\n    movzx rax,al\n"); break;
                case TOKEN_EQUAL:         fprintf(g->output,"    cmp rax,rbx\n    sete  al\n    movzx rax,al\n"); break;
                case TOKEN_NOT_EQUAL:     fprintf(g->output,"    cmp rax,rbx\n    setne al\n    movzx rax,al\n"); break;
                case TOKEN_AND:
                    fprintf(g->output,"    test rax,rax\n    setnz al\n");
                    fprintf(g->output,"    test rbx,rbx\n    setnz bl\n");
                    fprintf(g->output,"    and  al,bl\n    movzx rax,al\n"); break;
                case TOKEN_OR:
                    fprintf(g->output,"    test rax,rax\n    setnz al\n");
                    fprintf(g->output,"    test rbx,rbx\n    setnz bl\n");
                    fprintf(g->output,"    or   al,bl\n    movzx rax,al\n"); break;
                default: fprintf(stderr,"Unsupported operator\n"); exit(1);
            }
            break;
        }

        case AST_UNARY_OP:
            generate_expression(g, expr->data.unary.operand);
            if (expr->data.unary.op == TOKEN_NOT)
                fprintf(g->output,"    test rax,rax\n    setz al\n    movzx rax,al\n");
            else if (expr->data.unary.op == TOKEN_MINUS)
                fprintf(g->output,"    neg  rax\n");
            break;

        case AST_INDEX:
            generate_expression(g, expr->data.index_expr.object);
            fprintf(g->output,"    mov  r10, rax\n");
            generate_expression(g, expr->data.index_expr.index);
            fprintf(g->output,"    mov  rbx, rax\n    imul rbx, 8\n    add  rbx, 16\n");
            fprintf(g->output,"    add  rbx, r10\n    mov  rax, [rbx]\n"); break;

        case AST_FUNCTION_CALL: {
            const char* fname = expr->data.func_call.name;
            int nargs = expr->data.func_call.arg_count;
            if (strcmp(fname,"output")==0 && nargs>0)
                { emit_output(g,expr->data.func_call.arguments[0]); break; }
            if (strcmp(fname,"len")==0 && nargs==1) {
                generate_expression(g,expr->data.func_call.arguments[0]);
                fprintf(g->output,"    mov  rdi, rax\n    call list_len\n"); break;
            }
            if (strcmp(fname,"push")==0 && nargs==2) {
                generate_expression(g,expr->data.func_call.arguments[0]);
                fprintf(g->output,"    push rax\n");
                generate_expression(g,expr->data.func_call.arguments[1]);
                fprintf(g->output,"    mov  rsi, rax\n    pop  rdi\n    call list_push\n"); break;
            }
            for (int i=0;i<nargs&&i<MAX_REG_ARGS;i++){
                generate_expression(g,expr->data.func_call.arguments[i]);
                fprintf(g->output,"    push rax\n");
            }
            for (int i=(nargs<MAX_REG_ARGS?nargs:MAX_REG_ARGS)-1;i>=0;i--)
                fprintf(g->output,"    pop  %s\n",ARG_REGS[i]);
            fprintf(g->output,"    call %s\n",fname); break;
        }

        default: break;
    }
}

/* ─────────────────────────────────────────
   STATEMENT GENERATOR
───────────────────────────────────────── */
static void generate_statement(CodeGenerator* g, ASTNode* stmt) {
    if (!stmt) return;
    switch (stmt->type) {

        case AST_VAR_DECL: {
            current_offset += 8;
            ASTNode* init = stmt->data.var_decl.initializer;
            int is_list   = (init && init->type==AST_LIST_LITERAL)?1:0;
            int is_string = (init && init->type==AST_STRING)?1:0;
            int is_bool   = 0;
            if (init) {
                if (init->type == AST_BOOL) is_bool=1;
                else if (init->type==AST_UNARY_OP && init->data.unary.op==TOKEN_NOT) is_bool=1;
                else if (init->type==AST_BINARY_OP) {
                    TokenType op=init->data.binary.op;
                    if (op==TOKEN_EQUAL||op==TOKEN_NOT_EQUAL||
                        op==TOKEN_LESS||op==TOKEN_LESS_EQUAL||
                        op==TOKEN_GREATER||op==TOKEN_GREATER_EQUAL||
                        op==TOKEN_AND||op==TOKEN_OR) is_bool=1;
                }
                if (init->type==AST_IDENTIFIER && get_variable_is_bool(init->data.identifier))
                    is_bool=1;
                if (init->type==AST_FUNCTION_CALL &&
                    strncmp(init->data.func_call.name,"is",2)==0) is_bool=1;
            }
            add_variable(stmt->data.var_decl.name,current_offset,is_string,is_list,is_bool);
            fprintf(g->output,"    sub  rsp, 8\n");
            if (is_list) {
                int count=init->data.list_literal.count;
                fprintf(g->output,"    mov  rdi, %d\n    call list_alloc\n",count);
                fprintf(g->output,"    mov  [rbp - %d], rax\n",current_offset);
                for (int i=0;i<count;i++){
                    fprintf(g->output,"    mov  r10, [rbp - %d]\n",current_offset);
                    generate_expression(g,init->data.list_literal.elements[i]);
                    fprintf(g->output,"    mov  [r10 + %d], rax\n",16+i*8);
                }
            } else if (is_string) {
                int id=register_string(init->data.string_value);
                fprintf(g->output,"    lea  rax, [rel str%d]\n    mov  [rbp - %d], rax\n",id,current_offset);
            } else if (init) {
                generate_expression(g,init);
                fprintf(g->output,"    mov  [rbp - %d], rax\n",current_offset);
            }
            break;
        }

        case AST_LIST_ASSIGN: {
            int off=get_variable_offset(stmt->data.list_assign.name);
            if (off<0){fprintf(stderr,"Undefined list '%s'\n",stmt->data.list_assign.name);exit(1);}
            fprintf(g->output,"    mov  r10, [rbp - %d]\n",off);
            generate_expression(g,stmt->data.list_assign.index);
            fprintf(g->output,"    mov  r11, rax\n");
            generate_expression(g,stmt->data.list_assign.value);
            fprintf(g->output,"    imul r11, 8\n    add  r11, 16\n    add  r11, r10\n");
            fprintf(g->output,"    mov  [r11], rax\n"); break;
        }

        case AST_FUNCTION_CALL: {
            if (strcmp(stmt->data.func_call.name,"output")==0 &&
                stmt->data.func_call.arg_count>0)
                emit_output(g,stmt->data.func_call.arguments[0]);
            else {
                ASTNode tmp; tmp.type=AST_FUNCTION_CALL;
                tmp.data.func_call=stmt->data.func_call;
                generate_expression(g,&tmp);
            }
            break;
        }

        case AST_ASSIGNMENT: {
            int off=get_variable_offset(stmt->data.assignment.name);
            if (off<0){fprintf(stderr,"Undefined var '%s'\n",stmt->data.assignment.name);exit(1);}
            generate_expression(g,stmt->data.assignment.value);
            fprintf(g->output,"    mov  [rbp - %d], rax\n",off); break;
        }

        case AST_RETURN:
            if (stmt->data.return_value)
                generate_expression(g,stmt->data.return_value);
            fprintf(g->output,"    jmp  .L_ret_%s\n",g->current_function); break;

        case AST_IF: {
            int else_l=new_label(g), end_l=new_label(g);
            generate_expression(g,stmt->data.if_stmt.condition);
            fprintf(g->output,"    cmp  rax, 0\n    je   .L%d\n",else_l);
            generate_statement(g,stmt->data.if_stmt.then_branch);
            fprintf(g->output,"    jmp  .L%d\n.L%d:\n",end_l,else_l);
            if (stmt->data.if_stmt.else_branch)
                generate_statement(g,stmt->data.if_stmt.else_branch);
            fprintf(g->output,".L%d:\n",end_l); break;
        }

        /*
         * WHILE loop with break/skip support:
         *
         *   .Lstart:          ← continue_label (skip jumps here)
         *     evaluate condition
         *     cmp rax, 0
         *     je  .Lend       ← break_label    (break  jumps here)
         *     body
         *     jmp .Lstart
         *   .Lend:
         */
        case AST_WHILE: {
            int start = new_label(g);
            int end   = new_label(g);
            /* skip → re-evaluate condition (jump to start) */
            push_loop(end, start);
            fprintf(g->output, ".L%d:\n", start);
            generate_expression(g, stmt->data.while_stmt.condition);
            fprintf(g->output, "    cmp  rax, 0\n    je   .L%d\n", end);
            generate_statement(g, stmt->data.while_stmt.body);
            fprintf(g->output, "    jmp  .L%d\n", start);
            fprintf(g->output, ".L%d:\n", end);
            pop_loop();
            break;
        }

        /*
         * FOR loop with break/skip support:
         *
         *   init
         *   .Lstart:
         *     evaluate condition
         *     cmp rax, 0
         *     je  .Lend         ← break jumps here
         *     body
         *   .Lupdate:           ← skip  jumps here (run update first)
         *     update
         *     jmp .Lstart
         *   .Lend:
         *
         * The separate .Lupdate label ensures that  skip  still
         * runs  i = i + 1  before re-checking the condition,
         * which is the correct behaviour (same as C's continue).
         */
        case AST_FOR: {
            int start  = new_label(g);
            int update = new_label(g);
            int end    = new_label(g);
            /* skip → run update then loop (jump to .Lupdate) */
            push_loop(end, update);

            if (stmt->data.for_stmt.init)
                generate_statement(g, stmt->data.for_stmt.init);

            fprintf(g->output, ".L%d:\n", start);
            if (stmt->data.for_stmt.condition) {
                generate_expression(g, stmt->data.for_stmt.condition);
                fprintf(g->output, "    cmp  rax, 0\n    je   .L%d\n", end);
            }
            generate_statement(g, stmt->data.for_stmt.body);

            fprintf(g->output, ".L%d:\n", update);
            if (stmt->data.for_stmt.update)
                generate_statement(g, stmt->data.for_stmt.update);
            fprintf(g->output, "    jmp  .L%d\n", start);
            fprintf(g->output, ".L%d:\n", end);
            pop_loop();
            break;
        }

        /*
         * break — jump to the end label of the innermost loop
         */
        case AST_BREAK:
            fprintf(g->output, "    jmp  .L%d\n", current_break_label());
            break;

        /*
         * skip — jump to the continue label of the innermost loop
         *   while → jumps to .Lstart  (re-checks condition)
         *   for   → jumps to .Lupdate (runs update, then re-checks)
         */
        case AST_SKIP:
            fprintf(g->output, "    jmp  .L%d\n", current_continue_label());
            break;

        case AST_BLOCK:
            for (int i=0;i<stmt->data.block.statement_count;i++)
                generate_statement(g,stmt->data.block.statements[i]);
            break;

        default: break;
    }
}

/* ── function generator ── */
static void generate_function(CodeGenerator* g, ASTNode* func) {
    const char* name    = func->data.function.name;
    int         nparams = func->data.function.param_count;

    free_var_list();
    g->current_function = name;
    bool_str_yes_id = -1;
    bool_str_no_id  = -1;
    loop_depth = 0;   /* reset loop stack per function */

    if (strcmp(name,"main")==0) fprintf(g->output,"_start:\n");
    else                        fprintf(g->output,"%s:\n",name);

    fprintf(g->output,"    push rbp\n    mov  rbp, rsp\n");

    for (int i=0;i<nparams&&i<MAX_REG_ARGS;i++){
        current_offset+=8;
        add_variable(func->data.function.params[i],current_offset,0,0,0);
        fprintf(g->output,"    sub  rsp, 8\n");
        fprintf(g->output,"    mov  [rbp - %d], %s\n",current_offset,ARG_REGS[i]);
    }

    generate_statement(g,func->data.function.body);

    fprintf(g->output,".L_ret_%s:\n",name);
    fprintf(g->output,"    mov  rsp, rbp\n    pop  rbp\n");

    if (strcmp(name,"main")==0)
        fprintf(g->output,"    mov  rax, 60\n    mov  rdi, 0\n    syscall\n\n");
    else
        fprintf(g->output,"    ret\n\n");
}

/* ── public API ── */
CodeGenerator* create_code_generator(const char* filename) {
    CodeGenerator* g = safe_malloc(sizeof(CodeGenerator));
    g->output = fopen(filename,"w");
    if (!g->output){fprintf(stderr,"Cannot open '%s'\n",filename);exit(1);}
    g->label_counter=0; g->stack_offset=0; g->current_function="";
    var_list=NULL; current_offset=0; str_count=0; data_section[0]='\0';
    bool_str_yes_id=-1; bool_str_no_id=-1; loop_depth=0;
    return g;
}

void free_code_generator(CodeGenerator* g) {
    if (!g) return;
    if (g->output) fclose(g->output);
    free_var_list(); free(g);
}

void generate_code(CodeGenerator* g, ASTNode* program) {
    generate_prologue(g);
    if (!program || program->type!=AST_PROGRAM) return;
    for (int pass=0;pass<2;pass++) {
        for (int i=0;i<program->data.program.function_count;i++){
            ASTNode* func=program->data.program.functions[i];
            if (!func||func->type!=AST_FUNCTION) continue;
            int is_main=strcmp(func->data.function.name,"main")==0;
            if (pass==0&&is_main) continue;
            if (pass==1&&!is_main) continue;
            generate_function(g,func);
        }
    }
    if (str_count>0){
        fprintf(g->output,"\nsection .data\n");
        fprintf(g->output,"%s",data_section);
    }
    printf("Code generation completed: output.asm\n");
}